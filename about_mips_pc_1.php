<div class="about_title">SCORING THE ADVANCING CARE INFORMATION PERFORMANCE CATEGORY</div>
<br/><br/>

*formerly known as the Meaningful Use (MU) of EHR Technology

<br/><br/>
<center><img src="assets/images/mips_categories_ACI.png" width="100%"></center>

<br/><br/>

CMS proposes to score clinicians for both participation and performance using a “base score” and a “performance score” under the Advancing Care Information performance category. To earn points toward the base score (a total of 50 percentage points), providers must report the numerator and denominator (or yes/no statement, as applicable) for each of six objectives and their associated measures. Failure to meet any of the objectives would result in a base score of zero and an Advancing Care Information performance category score of zero.
<br/><br/>
The performance score (a total of 80 percentage points) is calculated based on performance on associated measures related to the Patient Electronic Access to Health Information, Coordination of Care Through Patient Engagement, and Health Information Exchange objectives.
<br/><br/>
Further, up to one bonus point is available for additional Public Health and Clinical Data Registry reporting.
<br/><br/>
A clinician’s base score, performance score, and bonus point (if applicable) are added
together for a total of up to 131 points. If a clinician earns 100 points or more, then the
clinician receives the full 25 points in the Advancing Care Information performance
category. If the clinician earns less than 100 points, the clinician’s overall score in MIPS
declines proportionately.
