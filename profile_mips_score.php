<div class="about_title">THE MIPS COMPOSITE SCORE</div>

CMS proposes a unified, composite performance scoring system to keep the scoring as simple as possible.
<br/><br/>
The following characteristics are suggested to be incorporated into the proposed scoring methodologies for each of the four performance categories:
<br/><br/>

<ul>
<li>For the Quality and Cost performance categories, each measure would be scored using a 10-point scoring system, with comparisons to historical benchmarks (if available) to assess improvement.</li>
<li>Performance at any level would receive points towards the performance category scores.
</li>
<li>CMS is not looking to include an “all or nothing” reporting requirement for MIPS, but clinicians who fail to report on an applicable measure or activity will receive the lowest possible score of zero points.
 </li>
<li>CMS will consider risk factors in the development of the scoring methodologies, and the agency will use measure-specific risk adjustment for all measures (where applicable) included in the Quality and Cost performance categories for the first year of MIPS.</li>
<li>The measure and activity performance standards would be published, where feasible, before the performance period begins.</li>
<li>The scoring proposals offer incentives to invest and focus on certain measures and activities that meet high-priority goals.</li>
</ul>


