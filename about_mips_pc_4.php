<br/><br/>
Due to Improvement Activities being a new performance activity with no ability to compare a clinicianâ€™s performance to baseline data, CMS has proposed assigning points for each reported activity within two categories: medium-weighted activities (worth 10 points) and highweighted activities (worth 20 points). High-weighted activities are specific activities related to:
<br/><br/>
<ul>
<li>Expanded Practice Access</li>
<li>Population Management</li>
<li>Care Coordination</li>
<li>Beneficiary Engagement</li>
<li>Patient Safety and Practice Assessment</li>
<li>Achieving Health Equity</li>
<li>Integrated Behavioral and Mental Health</li>
</ul>
<br/><br/>
Any activity not listed as a high-weighted activity is considered to be a mediumweighted
activity. For small practices, clinicians in rural areas or Health Professional Shortage Areas, or non-patient-facing clinicians, CMS believes that performing at least two Improvement Activities is achievable and proposes to award these clinicians 30 points for any activity selected.
<br/><br/>
For the calendar year 2017 performance period, CMS has proposed a highest potential score of 60 points. Clinicians participating in a patient-centered medical home or comparable specialty practice will receive the full 60 points.
<br/><br/>
In addition, clinicians participating in an APM will earn a minimum score of one-half the highest potential score, or at least 30 points. This is one example of how CMS proposes to provide clinicians participating in APMs with certain advantages under MIPS that could help the clinicians achieve positive MIPS payment adjustments. Although CMS has set a high bar for clinicians to receive the 5 percent Advanced APM bonus payment, and not all clinicians who participate in an APM will meet the criteria for the Advanced APM bonus payment, CMS does want to encourage APM participation. CMS therefore proposes to align standards, when possible, between the two components of the Quality Payment Program (MIPS and Advanced APMs) in order to make it easier for clinicians to move between them.
