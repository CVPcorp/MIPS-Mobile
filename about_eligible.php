<div class="about_title">ELIGIBILITY FOR YEARS 2017- 2018</div>

For the first two performance years of MIPS, the following Medicare Part B providers are deemed eligible professionals:

<ul>
<li>Physicians</li>
<li>Physician assistants</li>
<li>Nurse practitioners</li>
<li>Clinical nurse specialists</li>
<li>Nurse anesthetists</li>
</ul>


<hr class="divider">

<div class="about_title">ELIGIBILITY FOR YEARS 2019 ONWARDS</div>

<ul>
<li>Physical or occupational therapists</li>
<li>Speech-language pathologists</li>
<li>Audiologists</li>
<li>Nurse midwives</li>
<li>Clinical social workers</li>
<li>Clinical psychologists</li>
<li>Dietitians or nutrition professionals</li>
</ul>

<hr class="divider">

<div class="about_title">MORE</div>

<br/>
Provider groups including such professionals for each respective year are also deemed "eligible professionals" for purposes of measurement and payment adjustment by means to be defined in the CMS final rules.
<br/><br/>
Some components of MIPS, such as PQRS, already support the concept of group measurement. However, other parts such as MU only measure individual providers; therefore, the CMS final rules may need to define how individual MU performance is rolled up to the group level when needed for MIPS purposes.
<br/><br/>
There are three notable exclusions of providers from MIPS eligibility.
<br/><br/>
First, providers participating in an "alternative payment model" (see section 5 below) may not be subject to MIPS.
Second, CMS will define a low-volume threshold that includes a combination of minimum Medicare patients, service volume, and/or billings below which a provider is excluded from MIPS.
Third, providers who enroll in Medicare for the first time during a performance year are exempt from MIPS until the subsequent performance year.
