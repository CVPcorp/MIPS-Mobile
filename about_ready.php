<div class="about_title">1.	IMPLEMENT CERTIFIED EHR TECHNOLOGY. </div>
<br/><br/>

A well-designed certified electronic health
record (EHR) can help providers fulfill Meaningful Use and PQRS requirements with
much less effort. EHRs can provide real-time summaries of quality measure
progress and can be used to generate attestation documentation simplifying
administrative efforts to successfully meet program goals. (For solitary or small group practices: Given the need for practices to engage skilled resources to achieve high measure capture rates in EHR platforms, it may be difficult for solitary or small group practices to stay on top of the technical aspects associated with data reporting through EHRs. Economies of scale allow larger organizations to support skilled labor with expertise in quality reporting, potentially placing smaller practices at a disadvantage. <br/><br/>
To counter this CMS has proposed funding to assist small practices (defined as 15 or fewer physicians), in particular in rural and disadvantaged areas, that may occur through Regional Extension Centers and other third parties. However, training may not be adequate for some smaller practices that may require ongoing support from knowledgeable professionals to achieve high levels of performance.)

<br/><br/>

<div class="about_title">2.	MEET CURRENT QUALITY REPORTING PROGRAM MEASURES.
 </div>

<br/><br/>
MIPS will combine and streamline these existing EHR Incentive Program and pay-for- performance program
measurements into one program: <br/>

<ul>
<li>Meaningful Use (MU)</li>
<li>Physician Quality Reporting System (PQRS)</li>
<li>Value-based Payment Modifier (VM)</li>
</ul>

<br/><br/>

The sooner you start meeting the existing program measures successfully, the better prepared you’ll be to earn a higher payment bonus once MIPS starts.

<br/><br/>

<div class="about_title">3. KEEP TRACK OF MIPS</div>
<br/><br/>

MIPS. In order to maximize your Medicare bonuses over the long term, keep track of all MIPS requirements and other components of the Medicare Access &amp; CHIP Reauthorization Act (MACRA) legislation. CMS will likely update requirements on an annual basis, so keeping up to date can help make sure you meet and surpass all requirements each year. <br/><br/>Check updates from the MIPS Mobile App or the CMS website for updates on MACRA and MIPS.
<br/><br/>

<div class="about_title">4.	PREPARE FOR CPIA</div>
<br/><br/>

In addition to the existing quality programs, MIPS includes an additional category referred to as clinical practice improvement activities (CPIA). Due to CPIA being a new performance activity with no ability to compare a clinician’s
performance to baseline data, CMS has proposed assigning points for each reported
activity within two categories: medium-weighted activities (worth 10 points) and high-weighted activities (worth 20 points). High-weighted activities are specific activities

<br/><br/>
<ul>
<li>Expanded Practice Access</li>
<li>Population Management</li>
<li>Care Coordination</li>
<li>Beneficiary Engagement</li>
<li>Patient Safety and Practice Assessment</li>
<li>Achieving Health Equity</li>
<li>Integrated Behavioral and Mental Health</li>
</ul>

<br/><br/>

Any activity not listed as a high-weighted activity is considered to be a medium-weighted activity. For small practices, clinicians in rural areas or Health Professional Shortage Areas, or non-patient- facing clinicians, CMS believes that performing at least two CPIA activities is achievable and proposes to award these clinicians 30 points for any activity selected.
