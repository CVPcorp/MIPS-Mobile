<br/><br/>
<center><img src="assets/images/mips_categories_resourceuse.png" width="100%"></center>

<br/><br/>
Scoring for the Resource Use performance category is analogous to the scoring for the
Quality performance category, including assigning one to 10 points to each measure
based on a clinician’s performance compared to a benchmark and requiring a 20-case minimum for each resource use measure.
<br/><br/>
The benchmark for the Resource Use performance category will be based on the performance period, unlike the use of a baseline period for the Quality performance category.
