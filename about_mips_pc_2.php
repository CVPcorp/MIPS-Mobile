<div class="about_title">SCORING THE QUALITY PERFORMANCE CATEGORY</div>
<br/><br/>

<br/><br/>
<center><img src="assets/images/mips_categories_quality.png" width="100%"></center>
<br/><br/>
CMS proposes assigning one to 10 points for each measure based on how a MIPS eligible clinician’s performance compares to specific benchmarks. The benchmarks would be determined based on performance measures in the baseline period and would be published prior to the performance year, if possible. Clinicians must report a minimum of 20 cases for all quality measures, with the exception of the all-cause hospital readmission measure, which has a minimum of 200 cases. For clinicians who are unable to meet the case minimum requirement (such as solo practitioners), CMS offers various scoring exceptions to ensure that their quality performance scores are not unfairly skewed.
<br/><br/>
To incentivize clinicians to report high-priority measures (outcome, appropriate use, patient safety, efficiency, patient experience, and care coordination measures), CMS proposes providing two bonus points for each outcome and patient experience measure reported and one bonus point for other high-priority measures that are reported (in addition to the already required one high-priority measure). Clinicians will receive bonus points only if the performance rate is greater than zero, and CMS proposes capping bonus points for high-priority measures at either 5 percent or 10 percent of the denominator of the quality performance score to prevent clinicians from using bonus points to mask poor performance.
<br/><br/>
CMS proposes providing an additional bonus point under the quality performance measure to incentivize clinicians to use certified EHR technology (“CEHRT”). To be eligible for the CEHRT bonus, the following requirements (known as “end-to-end electronic reporting”) must be met:
<br/><br/>
<ul>
<li>The MIPS eligible clinician uses CEHRT to record the measure’s demographic and clinical data elements.</li>
<li>The MIPS eligible clinician exports and transmits the measure data electronically to a third party.</li>
<li>The third party, such as a registry, uses automated software to aggregate the measure data, calculate measures, perform any filtering of measurement data, and submit the data electronically to CMS.</li>
</ul>
<br/><br/>
The CEHRT bonus would be capped in a manner similar to the cap for the high-priority measures bonus, and CMS seeks comments on whether a 5 percent or 10 percent cap is appropriate.
In terms of measuring improvement, CMS has proposed three options:
<br/><br/>
<ul>
<li>The approach currently used for the Hospital Value-Based Purchasing Program, where CMS assigns one to 10 points for achievement and one to nine points for improvement for each measure;</li>
<li>The approach currently used for the Medicare Shared Savings Program, where clinicians would receive a certain number of bonus points for the quality improvement category for improvement; and</li>
<li>An approach similar to that for assessing improvement for the Medicare Advantage five-star rating methodology, where clinicians would receive an overall “improvement measure score” by comparing the underlying numeric data for measures from the prior year with the data from measures for the performance period.</li>
</ul>
