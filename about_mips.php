<div class="about_title">What is MIPS?</div>

MIPS stands for the Merit-based Incentive Payment System and was created by CMS as a means to simplify complex value-based programs into a single program.

<br/><br/>

MIPS was created by the Medicare Access and CHIP Reauthorization Act of 2015 to streamline multiple value-based programs:

<ul>
<li>Meaningful Use (MU)</li>
<li>Physician Quality Reporting System (PQRS)</li>
<li>Value-Based Modifier (VBM). </li>
</ul>

<br/>
CMS proposes a unified, composite performance scoring system to keep the scoring as simple as possible.
